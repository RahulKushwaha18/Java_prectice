public class ArethmeticOpretor {
    public static void main(String[] args) {
        int a,b;
        a=9;
        b=7;
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a++);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(++a);
    }
}
